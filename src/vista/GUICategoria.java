    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        id = Integer.parseInt(txtCodigo.getText());
        nombre = txtNombre.getText();
        descipcion = txtDescripcion.getText();
        Categoria c = new Categoria(id, nombre, descipcion);
        boolean inserto = cc.insertarCategoria(c);
        if(inserto){
            JOptionPane.showMessageDialog(this, "Categoria insertada corectamente!!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Categoria NO insertada");
        }
        mostrarCategoria();
        limpiarCampos();

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        id = Integer.parseInt(txtCodigo.getText());
        Object data [][] = cc.buscarCategoria(id);
        if(data[0][0] != null){
            txtNombre.setText(data[0][1].toString());
            txtDescripcion.setText(data[0][2].toString());
        }
        else{
            JOptionPane.showMessageDialog(this, "Categoria no encontrada");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        id = Integer.parseInt(txtCodigo.getText());
        boolean elimino = cc.eliminarCategoria(id);
        if(elimino){
            JOptionPane.showMessageDialog(this, "Categoria eliminada correctamente");
        }
        else{
            JOptionPane.showMessageDialog(this, "Categoria no eliminada");
        }
        mostrarCategoria();
        limpiarCampos();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        id = Integer.parseInt(txtCodigo.getText());
        nombre = txtNombre.getText();
        descipcion = txtDescripcion.getText();
        Categoria c = new Categoria(id, nombre, descipcion);
        boolean actualizo = cc.actualizarCategoria(c);
        if(actualizo){
            JOptionPane.showMessageDialog(this, "actualizo correctamente");
        }
        else{
            JOptionPane.showMessageDialog(this, "No actualizo");
        }
        mostrarCategoria();
        limpiarCampos();
    }//GEN-LAST:event_btnActualizarActionPerformed

